# 限定符
# a*     : a出现0次或多次
# a+     : a出现1次或多次
# a?     : a出现0次或1次
# a{6}   : a出现6次
# a{2,6} : a出现2-6次
# a{2,}  : a出现2次以上

# 或运算符
# (a|b)     : 匹配a或b
# (ab)|(cd) : 匹配ab或cd

# 字符串类
# [abc]       : 匹配a或b或c
# [a-c]       : 匹配a或b或c
# [a-zA-Z0-9] : 匹配字母及及数字
# [^0-9]      : 匹配非数字字符

# 元字符
# \d : 匹配数字字符
# \D : 匹配非数字字符
# \w : 匹配单词字符(字母、数字、下划线)
# \W : 匹配非单词字符
# \s : 匹配空白符(包含换行符、Tab)
# \S : 匹配非空白符
# .  : 匹配任意字符(换行符除外)
# \b : 匹配字符的边界(全字匹配), 例如: \bword\b
# \B : 匹配非字符的边界(全字匹配)
# ^  : 匹配行首
# $  : 匹配行尾

# 贪婪/懒惰匹配
# <.+>  : 默认贪婪匹配"任意字符"
# <.+?> : 懒惰匹配"任意字符"

import re

string = "Hello, World!"
pattern = r"\w+"

result = re.findall(pattern, string)
print(result)  # 输出: ['Hello', 'World']